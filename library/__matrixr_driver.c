#ifdef   __MATRIXR_DRV_I2C__
static uint8_t _slaveAddress;
#endif


/* -------------------------------------------- PRIVATE FUNCTION DECLARATIONS */

void matrixr_displayLeft(uint8_t column, uint8_t columnNumber);
void matrixr_displayRight(uint8_t column, uint8_t columnNumber);


/* --------------------------------------------- PRIVATE FUNCTION DEFINITIONS */

void matrixr_displayLeft(uint8_t column, uint8_t columnNumber)
{
    columnNumber++;

    hal_gpio_csSet( 0 );
    hal_spiWrite(&columnNumber,1);
    hal_spiWrite(&column,1);
    hal_gpio_csSet( 1 );
}

void matrixr_displayRight(uint8_t column, uint8_t columnNumber)
{
    columnNumber++;

    hal_gpio_anSet( 0 );
    hal_spiWrite(&columnNumber,1);
    hal_spiWrite(&column,1);
    hal_gpio_anSet( 1 );
}

/* --------------------------------------------------------- PUBLIC FUNCTIONS */

#ifdef   __MATRIXR_DRV_SPI__

void matrixr_spiDriverInit(T_MATRIXR_P gpioObj, T_MATRIXR_P spiObj)
{
    hal_spiMap( (T_HAL_P)spiObj );
    hal_gpioMap( (T_HAL_P)gpioObj );
}

#endif
#ifdef   __MATRIXR_DRV_I2C__

void matrixr_i2cDriverInit(T_MATRIXR_P gpioObj, T_MATRIXR_P i2cObj, uint8_t slave)
{
    _slaveAddress = slave;
    hal_i2cMap( (T_HAL_P)i2cObj );
    hal_gpioMap( (T_HAL_P)gpioObj );
}

#endif
#ifdef   __MATRIXR_DRV_UART__

void matrixr_uartDriverInit(T_MATRIXR_P gpioObj, T_MATRIXR_P uartObj)
{
    hal_uartMap( (T_HAL_P)uartObj );
    hal_gpioMap( (T_HAL_P)gpioObj );
}

#endif


/* ----------------------------------------------------------- IMPLEMENTATION */


/* Generic write register function */
void matrixr_writeDataReg(uint8_t registerAdress, uint8_t dataValue)
{
    hal_gpio_csSet( 0 );
    hal_spiWrite(&registerAdress,1);
    hal_spiWrite(&dataValue,1);
    hal_gpio_csSet( 1 );

    hal_gpio_anSet( 0 );
    hal_spiWrite(&registerAdress,1);
    hal_spiWrite(&dataValue,1);
    hal_gpio_anSet( 1 );

}

/* Function display left and right character */
void matrixr_displayCharacter( uint8_t characterLeft, uint8_t characterRight )
{
    int i;
    int j;
  
    for( i = 7; i>=0; i-- )
    {
        switch( i )
        {
            case 0: j = 7;
            break;
            case 1: j = 6;
            break;
            case 2: j = 5;
            break;
            case 3: j = 4;
            break;
            case 4: j = 3;
            break;
            case 5: j = 2;
            break;
            case 6: j = 1;
            break;
            case 7: j = 0;
            break;
        }
        matrixr_displayLeft( _MATRIXR_ASCII[ characterLeft - 32 ][ i ], j );
    }

    for( i = 7; i>=0; i-- )
    {
        switch( i )
        {
            case 0: j = 7;
            break;
            case 1: j = 6;
            break;
            case 2: j = 5;
            break;
            case 3: j = 4;
            break;
            case 4: j = 3;
            break;
            case 5: j = 2;
            break;
            case 6: j = 1;
            break;
            case 7: j = 0;
            break;
        }
        matrixr_displayRight( _MATRIXR_ASCII[ characterRight - 32 ][ i ], j );
    }
}








